export type Language = keyof typeof translations;
export type TranslationKey = keyof typeof translations.he;

export const getTranslation = (key: TranslationKey, lang: Language = 'he'): string => {
  return translations[lang]?.[key] || translations.he[key] || key;
};

export const getCurrentLanguage = (): Language => {
  if (typeof window !== 'undefined') {
    return (localStorage.getItem('language') as Language) || 'he';
  }
  return 'he';
};

export const setCurrentLanguage = (lang: Language): void => {
  if (typeof window !== 'undefined') {
    localStorage.setItem('language', lang);
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  }
};